    public function auth_update($twitter_id,$username,$token,$secret_token) {
        $user = $this->find('first',array(
            'conditions' => array(
                'User.twitter_id' => $twitter_id,
                'User.username' => $username,
            ),
            'recursive' => -1)
        );
        if (!(empty($user['User']))) {//既に登録してあった場合
            $this->save(array(
                'User' => array(
                    'id' => $user['User']['id'],
                    'username' => $username,
                    'twitter_id' => $twitter_id,
                    'token' => $token,
                    'secret_token' => $secret_token,
                )));

        } else {//新規登録の場合
            $this->save(array(
                'User' => array(
                    'username' => $username,
                    'twitter_id' => $twitter_id,
                    'token' => $token,
                    'secret_token' => $secret_token,
                )));
        }
        $return = ($this->save()) ? $this->id : null;
        return $return;
    }

